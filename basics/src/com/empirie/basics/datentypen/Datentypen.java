package com.empirie.basics.datentypen;

public class Datentypen{

	public static void main(String[] args) {
		
		short 	neuShort	= 34;
		int   	neuInt		= 33;
		long	neuLong		= 32;
		float	neuFloat	= 3.14f;
		boolean	neuBoolean	= true;
		char	neuChar		= 'A';
		double	neuDouble	= 3.1452d;

		System.out.print(neuShort + "\n");
		System.out.print(neuInt + "\n");
		System.out.print(neuLong + "\n");
		System.out.print(neuFloat + "\n");
		System.out.print(neuBoolean + "\n");
		System.out.print(neuChar + "\n");
		System.out.print(neuDouble + "\n");
	}

}